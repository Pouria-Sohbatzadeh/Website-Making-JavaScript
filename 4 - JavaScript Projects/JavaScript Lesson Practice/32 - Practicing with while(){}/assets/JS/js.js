
// The variable that determines the starting loop, Just be sure that you create it using the "var" keyword
var i = 0

// In the brace|()|, we set the condition for the variable that we set as the starting loop, Thus setting the ending loop
while(i <= 5){
    // In the curled brace|{}|, We put the code that we want to be repeated in the loops
    console.log(`This is loop ${i}`)

    // And HERE, We determine how many loops are skipped, By applying an Assignment Mathematic operator to the variable that we set as the starting loop
    i++
}