
// The for(){} loop allows to repeat codes/
// By putting a "var" variable (Which determines the starting loop), A condition which sets the ending loop.
// And an Assignment Methamatic Operator which allows us to determine which loops will be skipped.
// These we put in the brace|()|of for(){}, Seperating them with semi-colon|;|.

// In the curled brace|{}|we simply put the code that we want to be repeated.

for (var rep = 0; rep <= 4; rep++) {
    console.log(`We are in loop ${rep}`) // As a result of this, The numbers 0 through 4 will be shown, As the value of rep (Which shows the current loop), Is increased by 1 with each loop.
}